'''
SYNTAX:
Literals - as expected
Non terminals - between <>. Should be one of the following:
    any expression - ?
    type - bool, int, str, list, etc (non builtins are fine)
    if - if <cond> <expr0, expr1, ...>
    while - while <cond> <expr0, expr1, ...>
    for - for <target, iter> <expr0, expr1, ...>
    function definition - funcdef <name> <arg0, arg1, ...> <expr0, expr1, ...>
    + - at least 1 occurrence
    * - at least 0 occurrence
'''